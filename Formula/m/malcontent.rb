class Malcontent < Formula
  desc "Supply Chain Attack Detection, via context differential analysis and YARA"
  homepage "https://github.com/chainguard-dev/malcontent"
  url "https://github.com/chainguard-dev/malcontent/archive/refs/tags/v1.7.0.tar.gz"
  sha256 "a0d53b624fb6f786939e10740d7633d88da7443044a21ea706b8eb2ca15e59fc"
  license "Apache-2.0"

  depends_on "go" => :build
  depends_on "pkgconf" => :build
  depends_on "yara"

  def install
    system "go", "build", *std_go_args(ldflags: "-s -w -X main.BuildVersion=#{version}", output: bin/"mal"), "./cmd/mal"
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/mal --version")

    (testpath/"test.py").write <<~PYTHON
      import subprocess
      subprocess.run(["echo", "execute external program"])
    PYTHON

    assert_match "program â€” execute external program", shell_output("#{bin}/mal analyze #{testpath}")
  end
end
